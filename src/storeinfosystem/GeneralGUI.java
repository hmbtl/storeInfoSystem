    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String itemName;
        String itemPrice = jTextField2.getText();
        String itemBasePrice = jTextField22.getText();
        String itemQuantity = jTextField3.getText();
        if(jCheckBox9.isSelected())
        {
        itemName = jTextField23.getText();
        
        if(!checkText(jTextField2) || !checkText(jTextField22) || !checkText(jTextField3))
          JOptionPane.showMessageDialog(null,"Düzgün ədəd daxil edin!!","Store İnfo",JOptionPane.INFORMATION_MESSAGE);
        else  
        ItemInfo.addItem(itemName, Double.parseDouble(itemPrice), Double.parseDouble(itemBasePrice),Integer.parseInt(itemQuantity));
        }
        else
        {
        itemName = jTextField1.getText();
        int quan = ItemInfo.getItemsQuantity(ItemInfo.findItemsID(itemName));
        if(!checkText(jTextField3))
          JOptionPane.showMessageDialog(null,"Düzgün ədəd daxil edin!!","Store İnfo",JOptionPane.INFORMATION_MESSAGE);
        else 
        ItemInfo.setItemsQuantity(ItemInfo.findItemsID(itemName), quan + Integer.parseInt(itemQuantity));
        }
        initialTable();
        bind();
        decorate();
    }//GEN-LAST:event_jButton1ActionPerformed

