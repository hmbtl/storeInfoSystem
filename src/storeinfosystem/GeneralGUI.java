    private boolean checkText(javax.swing.JTextField jTextField)
    {
        if("".equals(jTextField.getText()))
            return true;
        char[] txt=jTextField.getText().toCharArray();
        int i= txt.length;
        int a=0;
        for (int k=0; k<i;k++)
        {
            if (txt[k]=='0' || txt[k]=='1' || txt[k]=='2' || txt[k]=='3' ||
                    txt[k]=='4' || txt[k]=='5' || txt[k]=='6' || txt[k]=='7' ||
                    txt[k]=='8' || txt[k]=='9' || txt[k]=='.')
            a++;
        }
        if (a==i)
        return true;
        else return false;
    }
    
