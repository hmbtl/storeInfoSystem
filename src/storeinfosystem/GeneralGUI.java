    private void initialSaleTable()
    {
        
      long startDate = jXDatePicker6.getDate().getTime() - jXDatePicker5.getDate().getTime();
        double total =0;
        startDate = startDate /(1000*60*60*24) ;
        jLabel34.setText("Gün : " + Long.toString(startDate+1) + " gün");
        int[] soldIDs = SoldItem.getItemsbyDate();
        int k=0;
        double revenue=0;
        contentSale = new Object[soldIDs.length][6];
        for (int i=0;i<soldIDs.length;i++)
        {
         if(SoldItem.getSoldsDate(soldIDs[i]).getTime()<= jXDatePicker6.getDate().getTime()
                 && SoldItem.getSoldsDate(soldIDs[i]).getTime()>= jXDatePicker5.getDate().getTime())
         {
            total += SoldItem.getItemsPrice(soldIDs[i])*SoldItem.getItemsQuantity(soldIDs[i]);
            contentSale[k][0]=soldIDs[i];
            contentSale[k][1]=SoldItem.getName(soldIDs[i]);
            contentSale[k][2]=SoldItem.getItemsQuantity(soldIDs[i]);
            contentSale[k][3]=ItemInfo.getItemsBasePrice(SoldItem.getItemsID(soldIDs[i]));
            contentSale[k][4]=SoldItem.getItemsPrice(soldIDs[i]);
            contentSale[k][5]=SoldItem.getItemsQuantity(soldIDs[i])*(SoldItem.getItemsPrice(soldIDs[i])-ItemInfo.getItemsBasePrice(SoldItem.getItemsID(soldIDs[i])));
           k++;
           revenue+=SoldItem.getItemsQuantity(soldIDs[i])*(SoldItem.getItemsPrice(soldIDs[i])-ItemInfo.getItemsBasePrice(SoldItem.getItemsID(soldIDs[i])));
         }
        }
        sales.clear();
        for (int index=0;index<contentSale.length;index++)
         sales.add((String) contentSale[index][1]);
         AutoCompleteDecorator.decorate(jTextField25, sales, true);
        
        jLabel35.setText("Ümumi Alver : " + total + " AZN" );
        jLabel36.setText("Gəlir : " + revenue + " AZN");
        jTable6.setModel(new javax.swing.table.DefaultTableModel(
            contentSale,
            new String [] {
                "ID","Malın adı", "Miqdarı", "Qiyməti","Satışı","Gəlir"
            }
        ));
    }
